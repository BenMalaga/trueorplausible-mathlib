"""Tests for src.build_benchmark on a synthetic mini-pool (no network, no real data)."""

import json

import pytest

from src import build_benchmark as bb


def _row(decl_id, decl):
    return json.dumps({"declId": decl_id, "decl": decl}, ensure_ascii=False)


@pytest.fixture
def mini_pool(tmp_path):
    """Synthetic pool exercising every dedupe/filter branch of PRE_REGISTRATION.md §4."""
    lines = []
    # 20 clean theorems (several applicable operators each)
    for i in range(20):
        lines.append(_row(f"Good.thm{i}", f"theorem good{i} (n : ℕ) (h : {i} < n) : n + {i} ≤ n + {i + 1}"))
    # per-tactic duplicate rows (same declId repeated)
    lines.append(_row("Good.thm0", "theorem good0 (n : ℕ) (h : 0 < n) : n + 0 ≤ n + 1"))
    lines.append(_row("Good.thm1", "theorem ignored duplicate row"))
    # duplicate decl TEXT under a different declId
    lines.append(_row("Dup.text", "theorem good2 (n : ℕ) (h : 2 < n) : n + 2 ≤ n + 3"))
    # filter fodder
    lines.append(_row("Bad.def", "def notATheorem : ℕ := 3"))
    lines.append(_row("Bad.sorry", "theorem bad_sorry (n : ℕ) : n = n := by sorry"))
    lines.append(_row("Auto.thm._simp_1", "theorem auto_simp (n : ℕ) : n = n"))
    lines.append(_row("Auto.Foo.mk.injEq", "theorem auto_mk (n : ℕ) : n = n"))
    lines.append(_row("Bad.long", "theorem long_one (n : ℕ) : n = n ∧ " + "n = n ∧ " * 100 + "n = n"))
    lines.append(_row("Bad.manylines", "theorem many\n" * 13 + " (n : ℕ) : n = n"))
    # proof body to strip
    lines.append(_row("Good.proofbody", "theorem with_proof (n : ℕ) : 1 * n = n := by simp"))
    p = tmp_path / "pool.jsonl"
    p.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return p


def _build(mini_pool, tmp_path, n=10, seed=bb.SEED):
    out = tmp_path / "bench.jsonl"
    man = tmp_path / "manifest.json"
    manifest = bb.build(mini_pool, out, man, n_source=n, seed=seed)
    items = [json.loads(l) for l in out.read_text(encoding="utf-8").splitlines()]
    return manifest, items, out, man


def test_dedupe_and_filter_log(mini_pool, tmp_path):
    manifest, _, _, _ = _build(mini_pool, tmp_path)
    log = manifest["pipeline_log"]
    assert log["dropped_duplicate_declId"] == 2
    assert log["dropped_duplicate_decl_text"] == 1
    reasons = log["excluded_by_reason"]
    assert reasons["not_theorem_or_lemma"] == 1
    assert reasons["contains_sorry"] == 1
    assert reasons["autogenerated_declId"] == 2
    assert reasons["over_600_chars"] == 1
    assert reasons["over_12_lines"] == 1
    assert log["proof_body_stripped"] >= 1
    # pool smaller than N: takes everything, and SAYS so
    assert "sample_note" in log or log["sampled_source_theorems"] == 10


def test_fifty_fifty_pairing_and_provenance(mini_pool, tmp_path):
    _, items, _, _ = _build(mini_pool, tmp_path, n=21)
    trues = [i for i in items if i["label"] == "true"]
    falses = [i for i in items if i["label"] == "false"]
    assert len(trues) == len(falses) > 0
    by_pair = {}
    for it in items:
        by_pair.setdefault(it["pair_id"], []).append(it)
    for pair_id, pair in by_pair.items():
        assert len(pair) == 2
        labels = {p["label"] for p in pair}
        assert labels == {"true", "false"}
        # provenance: pair_id encodes source declId + operator; both items carry both
        for p in pair:
            assert p["pair_id"] == f"{p['source_decl_id']}#{p['operator']}"
            assert p["tier"] in ("F", "M", "N")
            assert p["family"] in ("A", "B")


def test_at_most_two_mutants_per_source(mini_pool, tmp_path):
    _, items, _, _ = _build(mini_pool, tmp_path, n=21)
    per_source = {}
    for it in items:
        if it["label"] == "false":
            per_source.setdefault(it["source_decl_id"], set()).add(it["operator"])
    assert per_source and all(len(ops) <= 2 for ops in per_source.values())


def test_verification_status_and_primary_set(mini_pool, tmp_path):
    _, items, _, _ = _build(mini_pool, tmp_path, n=21)
    for it in items:
        if it["label"] == "true":
            assert it["verification_status"] == "library_proved"
        elif it["family"] == "A":
            assert it["verification_status"] == "false_by_construction"
            assert it["primary_set"] is True
        else:
            assert it["verification_status"] == "pending_lean_verification"
            assert it["primary_set"] is False
    # the falses carry the exact edit
    assert all(i["edit"] for i in items if i["label"] == "false")


def test_length_control(mini_pool, tmp_path):
    _, items, _, _ = _build(mini_pool, tmp_path, n=21)
    by_pair = {}
    for it in items:
        by_pair.setdefault(it["pair_id"], {})[it["label"]] = it
    for pair in by_pair.values():
        diff = abs(pair["true"]["ws_token_count"] - pair["false"]["ws_token_count"])
        assert diff <= bb.LENGTH_CONTROL_MAX_WS_TOKEN_DIFF


def test_byte_for_byte_determinism(mini_pool, tmp_path):
    m1, _, out1, _ = _build(mini_pool, tmp_path / "a", n=15)
    m2, _, out2, _ = _build(mini_pool, tmp_path / "b", n=15)
    assert out1.read_bytes() == out2.read_bytes()
    assert m1["benchmark_sha256"] == m2["benchmark_sha256"]
    assert m1["content_hash_sha256"] == m2["content_hash_sha256"]


def test_seed_changes_output(mini_pool, tmp_path):
    m1, _, _, _ = _build(mini_pool, tmp_path / "a", n=15, seed=bb.SEED)
    m2, _, _, _ = _build(mini_pool, tmp_path / "b", n=15, seed=1)
    assert m1["content_hash_sha256"] != m2["content_hash_sha256"]


def test_manifest_records_pinned_revisions_and_hashes(mini_pool, tmp_path):
    manifest, _, out, man = _build(mini_pool, tmp_path)
    assert manifest["seed"] == 20260611
    src = manifest["source_dataset"]
    assert src["hf_revision"] == "03b2ea6c3cf0a55203596445722ebb61c2328889"
    assert src["mathlib_src_commit"] == "cf8e23a62939ed7cc530fbb68e83539730f32f86"
    assert len(manifest["benchmark_sha256"]) == 64
    on_disk = json.loads(man.read_text())
    assert on_disk["benchmark_sha256"] == manifest["benchmark_sha256"]
    assert "pairs_per_tier" in manifest and "mutants_per_operator" in manifest
